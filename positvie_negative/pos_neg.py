num = float(input("Enter a Number : "))
if num > 0:
    print(f"{num} is an Positive Number!")
elif num == 0:
    print(f"{num} is an Zero!")
else:
    print(f"{num} is an Negative Number")

# Output - 
# Enter a Number : 5
# 5 is an Positive Number
# Enter a Number : -1.45
# -1.45 is an Negative Number
# Enter a Number : 0
# 0 is an Zero